#include "fract_ol.h"

int	ft_gcd(int a, int b)
{
	int	i;
	int	j;
	int gcd;

	i = 1;
	while (i <= a)
	{
		if (a % i == 0 && b % i == 0)
			gcd = i;
		i++;
	}
	return gcd;
}